#!/usr/bin/env python3
"""
36. 有效的数独 (Valid Sudoku)
题目: 判断 9x9 数独板是否有效（只检查已填数字是否有效，不求解）。

算法: 哈希表记录每行、每列、每 3x3 子宫已出现的数字
时间复杂度: O(81) = O(1)
空间复杂度: O(81) = O(1)
"""

def is_valid_sudoku(board: list[list[str]]) -> bool:
    """
    核心思想：用三个字典分别记录每行、每列、每宫的数字出现情况。
    遍历每个单元格，如果遇到数字，检查它是否在对应行/列/宫中已出现过。
    9x9 数独共 9 行、9 列、9 个 3x3 子宫。
    """
    rows = [set() for _ in range(9)]
    cols = [set() for _ in range(9)]
    boxes = [set() for _ in range(9)]
    
    for i in range(9):
        for j in range(9):
            num = board[i][j]
            if num == '.':
                continue
            
            # 计算子宫索引 (0-8)
            box_index = (i // 3) * 3 + (j // 3)
            
            # 检查是否重复
            if num in rows[i] or num in cols[j] or num in boxes[box_index]:
                return False
            
            # 记录
            rows[i].add(num)
            cols[j].add(num)
            boxes[box_index].add(num)
    
    return True

