#include <iostream>
#include <vector>
#include <unordered_set>
using namespace std;

/**
 * 36. 有效的数独 (Valid Sudoku)
 * 题目: 判断 9x9 数独板是否有效（只检查已填数字是否有效，不求解）。
 * 
 * 算法: 哈希表记录每行、每列、每 3x3 子宫已出现的数字
 * 时间复杂度: O(81) = O(1)
 * 空间复杂度: O(81) = O(1)
 */
bool isValidSudoku(vector<vector<char>>& board) {
    vector<unordered_set<char>> rows(9);
    vector<unordered_set<char>> cols(9);
    vector<unordered_set<char>> boxes(9);
    
    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 9; j++) {
            char num = board[i][j];
            if (num == '.') continue;
            
            // 计算子宫索引 (0-8)
            int box_index = (i / 3) * 3 + (j / 3);
            
            // 检查是否重复
            if (rows[i].count(num) || cols[j].count(num) || boxes[box_index].count(num)) {
                return false;
            }
            
            // 记录
            rows[i].insert(num);
            cols[j].insert(num);
            boxes[box_index].insert(num);
        }
    }
    
    return true;
}

